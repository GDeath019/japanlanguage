package com.example.japanlanguage;

public class translate {
    String japan;
    String vietNam;

    public translate(String japan, String vietNam) {
        this.japan = japan;
        this.vietNam = vietNam;
    }

    public String getJapan() {
        return japan;
    }

    public String getVietNam() {
        return vietNam;
    }

    public void setJapan(String japan) {
        this.japan = japan;
    }

    public void setVietNam(String vietNam) {
        this.vietNam = vietNam;
    }
}
